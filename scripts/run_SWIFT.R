# How to run SWIFT

# Not available in R, so follow steps below instead:

# Requires: Matlab, Statistics Toolbox, Parallel Computing Toolbox

# 1. Copy FCS data files into the directory SWIFT/D/T. Note that SWIFT will automatically
# perform an arcsinh transform, so the FCS files should not be transformed already.
# 2. Open SWIFT GUI from Matlab by typing "swift_main" in command window (from within the
# SWIFT program directory).
# 3. Set parameters and select markers.
# 4. Click on "Click to Run SWIFT".
# 5. Output files are saved in the same directory SWIFT/D/T.
